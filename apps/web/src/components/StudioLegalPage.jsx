export function StudioLegalPage({ docId, setActivePage }) {
  const doc = DOCS[docId] || DOCS.terms;

  return (
    <section className="panel legal-page" aria-labelledby="legal-page-title">
      <header className="legal-page__header">
        <div className="legal-page__tabs" role="tablist" aria-label="Legal documents">
          {[
            { id: "terms", label: "Terms of Service" },
            { id: "privacy", label: "Privacy Policy" },
            { id: "copyright", label: "Copyright" },
          ].map((t) => (
            <button
              key={t.id}
              type="button"
              role="tab"
              aria-selected={docId === t.id}
              className={`legal-page__tab${docId === t.id ? " legal-page__tab--active" : ""}`}
              onClick={() => setActivePage(t.id)}
            >
              {t.label}
            </button>
          ))}
        </div>
        <h2 id="legal-page-title">{doc.title}</h2>
        <p className="subtle legal-page__updated">Last updated: {doc.lastUpdated}</p>
      </header>
      <div className="legal-page__body">
        {doc.sections.map((sec) => (
          <article key={sec.h} className="legal-page__section">
            <h3>{sec.h}</h3>
            {sec.p.map((para, i) => (
              <p key={i}>{para}</p>
            ))}
          </article>
        ))}
      </div>
    </section>
  );
}
